                    <div id="readMoreContent" class="hidden">
                        <p data-block-key="xwl56">As long-distance travel opened up with the expansion of railway and
                            passenger steam lines in the 19th century, lightweight and convenient luggage options were
                            highly sought after. This record depicts a new design for an expanding railway basket
                            created by basket and luggage manufacturers Harriet J. Cave &amp; Sons. The railway basket
                            would have improved the comfort of travelling with a structure made of wicker, making it a
                            lighter alternative to the traditional wooden trunk. The innovation in the design came in

    <h2>Background</h2>
    <h3>Previous work</h3>
    <p>The aim of this version was to build on previous work carried out as part of work carried out to define the Information Architecture (IA) for Project ETNA.</p>
    <p>A discovery and alpha phase took place in Q1/Q2 of 2023. The outcome was a main navigation with 6 top-level categories. When an item was selected it would toggle a sub-navigation area. This sub-menu varied in its content dependent on the menu item selected, but the intention was for a user to gather context about a menu item and be able to navigate to the second level category without having to leave the page. </p>
    <p>Concepts were created in Figma and are available to view.</p>

    <h3>Brand refresh</h3>
    <p>Some of the global elements of the alpha and private beta stages have been created in a simplified and deliberately 'agnostic' way whilst evolution of the wider visual identity developed. This header is intended to work with an updated visual identity, but to also be usable if it is needed for use with legacy applications.</p>

    <h3>Design system foundations</h3>
    <hr>
    <h2>Design notes:</h2>
    <p>Highlights of the changes and rationale from the previous version include:</p>
    <h3>Deep header</h3>
    <p>Accomodating a square logo with text has been problematic in previous iterations, so this version aims to embrace the height of the logo and provide a deeper header area to ensure the logo has the space it needs to be legible and visible.</p>

    <h3>Three-row navigation presentation</h3>
    <p>By embracing the logo in a larger size, the navigation is extended onto 3-rows at larger screen sizes. This gives it a balance with the 3-row logo and moves away from a horizontal navigation to avoid issues with label length.</p>
